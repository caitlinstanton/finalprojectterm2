public class Switch {

    public static void wee(int ex){
    switch (ex) {
    case 0:
	System.out.println("This is zero!");
	break;
    case 34: 
	System.out.println("34 YO!");
	break;
    default:
	System.out.println("noooooooooooooooooo");
	break;
	    
    }
    }
    public static void main(String[]args){
	int t = 2456;

	wee(t);
    }
    

}
